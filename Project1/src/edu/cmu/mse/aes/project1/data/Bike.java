public class Bike {
	
	private String manufacurer;
	private String model;
	private double price;
	private String frameSize;
	private String frameMaterial;
	private String forkMaterial;
	private String componentInfo;
	private String consumerRating;
	public String getManufacurer() {
		return manufacurer;
	}
	
	public void setManufacurer(String manufacurer) {
		this.manufacurer = manufacurer;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getFrameSize() {
		return frameSize;
	}
	public void setFrameSize(String frameSize) {
		this.frameSize = frameSize;
	}
	public String getFrameMaterial() {
		return frameMaterial;
	}
	public void setFrameMaterial(String frameMaterial) {
		this.frameMaterial = frameMaterial;
	}
	public String getForkMaterial() {
		return forkMaterial;
	}
	public void setForkMaterial(String forkMaterial) {
		this.forkMaterial = forkMaterial;
	}
	public String getComponentInfo() {
		return componentInfo;
	}
	public void setComponentInfo(String componentInfo) {
		this.componentInfo = componentInfo;
	}
	public String getConsumerRating() {
		return consumerRating;
	}
	public void setConsumerRating(String consumerRating) {
		this.consumerRating = consumerRating;
	}
	
	

}
